def seed_extra_cards():
    db = SessionLocal()
    print("🚀 Начинаю загрузку дополнительных профессий и здоровья...")
    
    try:
        # Добавляем Профессии
        for name, desc in bunker_jobs:
            new_job = CardCreate(
                type=CardType.PROFESSION,
                name=name,
                description=desc,
                power_level=0,  # Для работы уровень не так важен
                skill_level=random.randint(1, 3), # Навык 1-3
                chaos_level=1
            )
            create_card(db, new_job)
            
        # Добавляем Здоровье
        for name, desc, power in bunker_health:
            new_health = CardCreate(
                type=CardType.HEALTH,
                name=name,
                description=desc,
                power_level=power, # ВАЖНО: записываем уровень здоровья!
                skill_level=0,
                chaos_level=1
            )
            create_card(db, new_health)
            
        print(f"✅ Успешно добавлено {len(bunker_jobs)} профессий и {len(bunker_health)} карточек здоровья!")
    except Exception as e:
        print(f"❌ Произошла ошибка: {e}")
    finally:
        db.close()


 
from app.services.new_card import card_biology, card_appearance

def seed_biology_and_appearance(count=15):

    db = SessionLocal()
    """Генерирует случайные карты Биологии и Внешности и сохраняет их в БД"""
    print(f"Начинаем генерацию {count} пар карт (Биология + Тело)...")
    
    for i in range(count):
        # --- 1. БИОЛОГИЯ ---
        bio_data = card_biology()
        # В БД: name = Раса, description = Пол и возраст
        bio_card = Card(
            type=CardType.BIOLOGY,
            name=bio_data['race'],
            description=f"Пол: {bio_data['gender']}, Возраст: {bio_data['old']} лет",
            power_level=0,  # Заглушки, так как для этих карт статы не так важны
            skill_level=0,
            chaos_level=0,
            is_used=False,
            is_revealed=False
        )
        db.add(bio_card)

        # --- 2. ТЕЛО (ВНЕШНОСТЬ) ---
        app_data = card_appearance()
        # В БД: name = Описание внешности (дрыщ/рама/и тд), description = Рост и вес
        app_card = Card(
            type=CardType.appearance, # Судя по твоему crud.py, у тебя этот тип написан с маленькой буквы
            name=app_data['appearance'],
            description=f"Рост: {app_data['height']} см, Вес: {app_data['mass']} кг",
            power_level=0,
            skill_level=0,
            chaos_level=0,
            is_used=False,
            is_revealed=False
        )
        db.add(app_card)

    # Сохраняем всё в базу данных одним разом
    try:
        db.commit()
        print(f"✅ Успешно добавлено {count} карт Биологии и {count} карт Внешности в базу данных!")
    except Exception as e:
        db.rollback()
        print(f"❌ Произошла ошибка при сохранении: {e}")

