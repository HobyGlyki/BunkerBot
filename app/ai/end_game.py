from ollama import AsyncClient
import asyncio
import time

semaphore = asyncio.Semaphore(1)  # Ограничиваем до 1 одновременного запроса к AI

# Задаем роль для нейросети
SYSTEM_PROMPT = """Ты — реалистичный, но ироничный искусственный интеллект, подводящий итоги игры "Бункер". 
Твоя задача: написать забавный, увлекательный, но логически обоснованный эпилог (3-4 абзаца) о том, как выжившие попытались возродить человечество. Никакого излишнего абсурда — опирайся на суровую реальность выживания в замкнутом пространстве.
Не используй мета-информацию, пиши лишь в рамках истории мира и персонажей.
Возрадить, значит родить несколько детей.

ОБЯЗАТЕЛЬНО начни свой ответ с одной из двух фраз, в зависимости от твоего вердикта:
"<b>Поздравляю, вам удалось выжить!</b>" ИЛИ "<b>К сожалению, вы не выжили...</b>"

Вердикт "Выжили" выносится ТОЛЬКО если логически соблюдены 4 правила:
1. В бункере есть хотя бы одна пара (мужчина и женщина в подходящем возрасте) для продолжения рода.
2. В команде нет критически слабых и абсолютно беспомощных людей, которые станут неподъемной обузой и потратят все ресурсы.
3. В бункер не пронесли смертельную, заразную болезнь, которая быстро уничтожит всех в закрытой вентиляции.
4. Профессии, инвентарь и хобби выживших используются на благо (добыча энергии, ремонт, лечение, синтез еды), а не ведут к быстрому саморазрушению.

Опирайся на:
- Катастрофу, которая уничтожила мир.
- Параметры бункера (еда, инвентарь, площадь).
- Характеристики выживших (раса/пол/возраст, профессии, болезни, инвентарь, фобии).

Опиши, как именно их навыки и предметы взаимодействовали в первые годы. Если они выжили — расскажи, как они преодолели трудности благодаря своим талантам. Если погибли — опиши, какое именно из 4 правил было нарушено и как это привело к забавному или трагичному концу.

Выдавай ответ СРАЗУ в виде HTML-текста (используй теги <p>, <b>, <i>, <br> для абзацев и выделений). Не используй Markdown-блок (```html) и другие символы форматирования кода."""

async def ai_response(game, players_text: str):
    start_time = time.perf_counter()

    # Собираем всю информацию в один текст для ИИ
    user_content = (
        f"Катастрофа: {game.disaster_description}\n"
        f"Параметры бункера: {game.bunker_features_json}\n\n"
        f"Список выживших и их характеристики:\n{players_text}\n\n"
        f"Напиши эпилог их выживания."
    )

    client = AsyncClient()
    print("Отправляем запрос на генерацию эпилога...")
    
    try:
        response = await client.chat(
            model='gemma4:31b-cloud',
            messages=[
                {'role': 'system', 'content': SYSTEM_PROMPT},
                {'role': 'user', 'content': user_content},
            ],
            options={
                "temperature": 0.9,     # Чуть больше хаоса для смешной истории
                "num_ctx": 4096,
                "num_predict": 2500,
            }
        )
        content = response['message']['content']
        return content
    except Exception as e:
        return f"<p style='color: #ff4757;'>❌ Ошибка связи с ИИ: {e}</p>"

async def generate_finale_ai(game, players_text: str):
    start_time = time.perf_counter()
    print(f"Генерация эпилога началась...")
    
    async with semaphore:
        result = await ai_response(game, players_text) 

    duration = time.perf_counter() - start_time
    print(f"Эпилог готов! Общее время: {duration:.2f} сек.")
    return result